#include "LevelData.h"

static void CopyLevelData(Level& level, int data[Level::ROWS][Level::COLS])
{
    for (int r = 0; r < Level::ROWS; r++) {
        for (int c = 0; c < Level::COLS; c++) {
            level.grid[r][c] = data[r][c];
        }
    }
}

